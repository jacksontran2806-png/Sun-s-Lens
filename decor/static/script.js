function isValidURL(url) {
  try {
    const parsed = new URL(url);
    return parsed.protocol === "http:" || parsed.protocol === "https:";
  } catch {
    return false;
  }
}

async function analyze() {
  const url = document.getElementById('url-input').value.trim();
  const btn = document.getElementById('analyze-btn');
  const skeletonCard = document.getElementById('skeleton-card');
  const resultCard = document.getElementById('result-card');
  const resultBody = document.getElementById('result-body');
  const resultUrl = document.getElementById('result-url');
  const errorMsg = document.getElementById('error-msg');

  if (!url) return;

  if (!isValidURL(url)) {
    errorMsg.classList.add('active');
    errorMsg.textContent = '⚠ Invalid URL. Make sure it starts with https://';
    return;
  }

  errorMsg.classList.remove('active');
  resultCard.classList.remove('active');
  skeletonCard.classList.add('active');
  btn.disabled = true;

  try {
    const response = await fetch('/analyze', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ url })
    });

    const data = await response.json();

    if (data.error) {
      errorMsg.classList.add('active');
      errorMsg.textContent = '⚠ ' + data.error;
    } else {
      resultUrl.textContent = url;
      resultBody.innerHTML = data.summary;
      resultCard.classList.add('active');
    }
  } catch (err) {
    errorMsg.classList.add('active');
    errorMsg.textContent = '⚠ Something went wrong. Please try again.';
  } finally {
    skeletonCard.classList.remove('active');
    btn.disabled = false;
  }
}

function copyResult() {
  const resultBody = document.getElementById('result-body');
  const btn = event.target;
  const text = resultBody.innerText;

  navigator.clipboard.writeText(text).then(() => {
    btn.textContent = '✅ Copied!';
    btn.classList.add('copied');
    setTimeout(() => {
      btn.textContent = '📋 Copy';
      btn.classList.remove('copied');
    }, 2000);
  });
}

function downloadPDF() {
  const url = document.getElementById('result-url').textContent;
  const resultBody = document.getElementById('result-body');
  const btn = event.target;

  btn.textContent = '⏳ Generating...';
  btn.disabled = true;

  const text = resultBody.innerText;
  const sourceUrl = url;

  const blob = new Blob([
    `LINKLENS SUMMARY\n`,
    `================\n`,
    `Source: ${sourceUrl}\n`,
    `Generated by LinkLens — by Jackson Tran\n`,
    `================\n\n`,
    text
  ], { type: 'text/plain' });

  const a = document.createElement('a');
  a.href = URL.createObjectURL(blob);
  a.download = 'linklens-summary.txt';
  a.click();
  URL.revokeObjectURL(a.href);

  btn.textContent = '✅ Downloaded!';
  setTimeout(() => {
    btn.textContent = '📄 Download';
    btn.disabled = false;
  }, 2000);
}

document.getElementById('url-input').addEventListener('keydown', e => {
  if (e.key === 'Enter') analyze();
});